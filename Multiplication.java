class Multiplication{
	public static void main(String[] args){
		int i, j, result;

		for(i=2;i<=9;i++)
		{
			//System.out.println(---------);
			for(j=1; j<=9; j++)
			{
			result=i*j;
			System.out.print(i+"*"+j+"="+result+"\n");
			}
			System.out.println("---------");
		}

		

	}
}